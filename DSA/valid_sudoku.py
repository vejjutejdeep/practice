def check_columns(arr):
        
    i = 0

    while i < len(arr[0]):

        col_dic = {}

        for j in range(len(arr)):

            if arr[j][i] == ".":

                continue

            elif arr[j][i] in col_dic:

                col_dic[arr[j][i]] += 1
            
            else:

                col_dic[arr[j][i]] = 1
            
        # print(col_dic.values())
        lis = sorted(col_dic.values())

        if lis[-1] != 1:

            return False

        i += 1
    return True

def check_rows(arr):

    i = 0

    while i < len(arr[0]):

        row_dic = {}

        for j in range(len(arr)):

            if arr[i][j] == ".":

                continue

            elif arr[i][j] in row_dic:

                row_dic[arr[i][j]] += 1
            
            else:

                row_dic[arr[i][j]] = 1
            
        # print(col_dic.values())
        lis = sorted(row_dic.values())

        if lis[-1] != 1:

            return False

        i += 1
    return True

        

def valid_sudoku(arr):
        
    if check_columns(arr):

        if check_rows(arr):

            return True


    return False

